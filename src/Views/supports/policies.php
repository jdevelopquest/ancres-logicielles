<section class="content">
    <div class="content__subcontent prominent">
        <h2 class="prominent__text">Charte de modération</h2>
    </div>
    <article class="content__subcontent">
        <div class="prominent">
            <h3 class="prominent__text">Principes fondamentaux</h3>
        </div>

        <ul class="regular">
            <li class="regular__text">
                Respect mutuel :
                <ul class="regular">
                    <li class="regular__text">
                        Tous les membres de la communauté sont tenus de se traiter
                        avec respect, courtoisie et
                        considération. Les attaques personnelles, les insultes, les discriminations et le harcèlement ne
                        sont
                        pas tolérés.
                    </li>
                </ul>
            </li>
            <li class="regular__text">
                Esprit de collaboration :
                <ul class="regular">
                    <li class="regular__text">
                        Ancres Logicielles est une plateforme collaborative
                        où chacun est encouragé à
                        partager ses connaissances, ses expériences et ses découvertes de manière constructive et
                        bienveillante.
                    </li>
                </ul>
            </li>
            <li class="regular__text">
                Qualité et pertinence des contenus :
                <ul class="regular">
                    <li class="regular__text">
                        Les liens partagés doivent être
                        pertinents, de qualité et en
                        rapport avec le thème des logiciels. Les liens trompeurs, spam, ou promotionnels sont interdits.
                    </li>
                </ul>
            </li>
            <li class="regular__text">
                Modération communautaire :
                <ul class="regular">
                    <li class="regular__text">
                        Chaque membre de la communauté est invité à
                        participer à la modération en
                        signalant les contenus inappropriés ou les comportements déviants.
                    </li>
                </ul>
            </li>
        </ul>

        <div class="regular">
            <p class="regular__text">Règles de conduite</p>
        </div>

        <ol class="regular">
            <li class="regular__text">Propos interdits :
                <ul class="regular">
                    <li class="regular__text">Discrimination et incitation à la haine (racisme, sexisme, homophobie,
                        etc.)
                    </li>
                    <li class="regular__text">Insultes, menaces, harcèlement et intimidation</li>
                    <li class="regular__text">Diffamation et atteinte à la réputation</li>
                    <li class="regular__text">Contenus violents, pornographiques ou illégaux</li>
                    <li class="regular__text">Spam, publicité et autopromotion</li>
                    <li class="regular__text">Usurpation d'identité</li>
                    <li class="regular__text">Divulgation d'informations personnelles (numéro de téléphone, adresse,
                        etc.)
                    </li>
                </ul>
            </li>
            <li class="regular__text">Comportements à éviter :
                <ul class="regular">
                    <li class="regular__text">Hors-sujet et pourriel</li>
                    <li class="regular__text">Dérives politiques ou religieuses</li>
                    <li class="regular__text">Polémiques stériles et conflits personnels</li>
                    <li class="regular__text">Jugements hâtifs et généralisations abusives</li>
                </ul>
            </li>
            <li class="regular__text">Modération des liens :
                <ul class="regular">
                    <li class="regular__text">Les liens partagés doivent être accompagnés d'une description claire
                        et concise du contenu.
                    </li>
                    <li class="regular__text">Les liens vers des sites malveillants ou non sécurisés sont
                        interdits.
                    </li>
                    <li class="regular__text">Les liens doivent être pertinents et en rapport avec le thème du
                        logiciel concerné.
                    </li>
                </ul>
            </li>
        </ol>
    </article>

    <article class="content__subcontent">
        <div class="prominent">
            <h3 class="prominent__text">Modération</h3>
        </div>
        <ul class="regular">
            <li class="regular__text">Signalement :
                <ul class="regular">
                    <li class="regular__text">
                        Tout membre de la communauté peut signaler un contenu ou un
                        comportement qu'il juge
                        inapproprié.
                    </li>
                </ul>
            </li>
            <li class="regular__text">Traitement des signalements :
                <ul class="regular">
                    <li class="regular__text">
                        Les modérateurs examineront les signalements et
                        prendront les mesures nécessaires en cas de violation de la présente charte.
                    </li>
                </ul>
            </li>
            <li class="regular__text">Les sanctions possibles incluent :
                <ul class="regular">
                    <li class="regular__text">Avertissement</li>
                    <li class="regular__text">Suppression du contenu incriminé</li>
                    <li class="regular__text">Suspension temporaire ou définitive du compte</li>
                </ul>
            </li>
            <li class="regular__text">Appel :
                <ul class="regular">
                    <li class="regular__text">
                        En cas de désaccord avec une décision de modération, il est possible
                        de contacter les
                        modérateurs pour faire appel.
                    </li>
                </ul>
            </li>
        </ul>
    </article>

    <article class="content__subcontent">
        <div class="prominent">
            <h3 class="prominent__text">Responsabilité</h3>
        </div>
        <div class="regular">
            <p class="regular__text">
                Chaque membre de la communauté est responsable de ses contributions et de son
                comportement sur la plateforme.
                La modération ne saurait être tenue responsable des propos tenus par les utilisateurs.
            </p>
        </div>
    </article>

    <article class="content__subcontent">
        <div class="prominent">
            <h3 class="prominent__text">Évolution de la charte</h3>
        </div>
        <div class="regular">
            <p class="regular__text">
                La présente charte de modération est susceptible d'évoluer en fonction des besoins
                de la communauté et des
                retours des utilisateurs. Les modifications seront portées à la connaissance des membres.
            </p>
        </div>
    </article>

    <article class="content__subcontent">
        <div class="prominent">
            <h3 class="prominent__text">Acceptation de la charte</h3>
        </div>
        <div class="regular">
            <p class="regular__text">
                En utilisant Ancres Logicielles, vous acceptez les termes de la présente charte de
                modération.
            </p>
        </div>
    </article>
</section>
